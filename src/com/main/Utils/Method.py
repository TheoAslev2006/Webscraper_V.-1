def CommandSwitch(lang):
    if lang == "help":
        return 1
    elif lang == "getTitle":
        return 2
    elif lang == "getBody":
        return 3
    elif lang == "getDiv":
        return 4
    elif lang == "getScript":
        return 5
    elif lang == "getLogs":
        return 6
    elif lang == "getLinks":
        return 7
    elif lang == "setLinks":
        return 8
    elif lang == "addLinks":
        return 9
